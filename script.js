function createBoard() {
  board.innerHTML = "";
  for (let r = 0; r < 7; r++) {
    const row = document.createElement("div");
    row.className = "row";
    row.id = `row${r}`;
    for (let c = 0; c < 5; c++) {
      const input = document.createElement("input");
      input.type = "text";
      input.maxLength = 1;
      input.className = "letter";
      input.id = `r${r}c${c}`;
      if (r !== 0) input.disabled = true;
      row.appendChild(input);
    }
    board.appendChild(row);
  }

  for (let r = 0; r < 7; r++) {
    for (let c = 0; c < 5; c++) {
      const cell = document.getElementById(`r${r}c${c}`);

      cell.addEventListener("input", () => {
        if (currentLang === "en") {
          cell.value = cell.value.toUpperCase().replace(/[^A-Z]/g, "");
        } else if (currentLang === "ru") {
          cell.value = cell.value.toUpperCase().replace(/[^А-ЯЁ]/g, "");
        } else if (currentLang === "ua") {
          cell.value = cell.value.toUpperCase().replace(/[^А-ЩЬЮЯЄІЇҐ]/g, "");
        }

        if (cell.value && c < 4) {
          document.getElementById(`r${r}c${c + 1}`).focus();
        }
      });

      cell.addEventListener("keydown", (e) => {
        if (e.key === "Backspace" && cell.value === "" && c > 0) {
          const prev = document.getElementById(`r${r}c${c - 1}`);
          prev.value = "";
          prev.focus();
          e.preventDefault();
        }

        if (e.key === "Enter") {
          if (r === currentRow) {
            e.preventDefault();
            checkWord();
          }
        }
      });
    }
  }
}

function checkWord() {
  const guess = [];
  for (let i = 0; i < 5; i++) {
    const val = document
      .getElementById(`r${currentRow}c${i}`)
      .value.toUpperCase();
    if (val.length !== 1) {
      message.textContent = i18n[currentLang].fillAll;
      return false;
    }
    guess.push(val);
  }

  const result = Array(5).fill("red");
  const secretArr = secret.split("");
  const guessCopy = [...guess];

  for (let i = 0; i < 5; i++) {
    if (guess[i] === secretArr[i]) {
      result[i] = "green";
      guessCopy[i] = null;
      secretArr[i] = null;
    }
  }

  for (let i = 0; i < 5; i++) {
    if (guessCopy[i] && secretArr.includes(guessCopy[i])) {
      result[i] = "yellow";
      secretArr[secretArr.indexOf(guessCopy[i])] = null;
    }
  }

  for (let i = 0; i < 5; i++) {
    const input = document.getElementById(`r${currentRow}c${i}`);
    setTimeout(() => {
      input.classList.add(result[i]);
      input.disabled = true;
    }, i * 300);
  }

  if (guess.join("") === secret) {
    message.textContent = i18n[currentLang].win;
    checkBtn.disabled = true;
    restartBtn.style.display = "inline-block";
    return true;
  }

  currentRow++;
  if (currentRow >= 7) {
    message.textContent = i18n[currentLang].lose(secret);
    checkBtn.disabled = true;
    restartBtn.style.display = "inline-block";
    return true;
  }

  setTimeout(() => {
    for (let i = 0; i < 5; i++) {
      const next = document.getElementById(`r${currentRow}c${i}`);
      next.disabled = false;
    }
    focusNextEmpty(currentRow);
    message.textContent = "";
  }, 1500);

  return true;
}

function resetGame() {
  currentRow = 0;
  secret =
    dictionaries[currentLang][
      Math.floor(Math.random() * dictionaries[currentLang].length)
    ];
  createBoard();
  message.textContent = "";
  checkBtn.disabled = false;
  restartBtn.style.display = "none";
  updateTexts();
}

function updateTexts() {
  title.textContent = i18n[currentLang].title;
  checkBtn.textContent = i18n[currentLang].checkBtn;
  restartBtn.textContent = i18n[currentLang].restartBtn;
}

function focusNextEmpty(row) {
  for (let i = 0; i < 5; i++) {
    const cell = document.getElementById(`r${row}c${i}`);
    if (cell.value === "") {
      cell.focus();
      return;
    }
  }
}

